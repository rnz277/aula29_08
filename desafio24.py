#indicado o valor das variaveis.
produto = float(input('Digite o valor do produto: '))

#indicado o desconto do produto.
desconto = produto*0.10

#o valor total e indicado
vl_total = produto - desconto

#o resultado final do da equação.
print(f'O produto {produto} recebe o desconto de {desconto} e o total é {vl_total} ')